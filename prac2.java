package org.example;

public class prac2 {
    public static void main(String[] args) {
        System.out.println("증가");
        int k = 1;
        while (k <= 10) {
            System.out.println(k);
            k++;
        }
        System.out.println("감소");
        int i = 10;
        while (i >= 1) {
            System.out.println(i);
            i -= 3;
        }
    }
}
