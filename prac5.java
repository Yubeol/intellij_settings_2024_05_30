package org.example;

public class prac5{
    public static void main(String[] args) {
        System.out.println("=== while ===");
        int i = 1;
        while (i <= 3) {
            System.out.println(i);
            i++;
        }
        System.out.println("=== for ===");

        for (int j = 1; j <=3; j++){
            System.out.println(j);
        }
    }
}
